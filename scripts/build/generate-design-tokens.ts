import { mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';

import { designTokenDefinitions, type DesignTokenDefinition } from '../../components/style/tokens/definitions';

const repoRoot = path.resolve(__dirname, '../..');
const cssVariablesPath = path.join(repoRoot, 'components/style/themes/token-css-variables.less');
const mappingPath = path.join(repoRoot, 'components/style/tokens/token-map.md');

function toKebabCase(value: string): string {
  return value
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/\./g, '-')
    .toLowerCase();
}

function toCssVariableName(token: DesignTokenDefinition): string {
  if (token.cssVariable) {
    return token.cssVariable.replace('--ant-', '--@{ant-prefix}-');
  }

  return `--@{ant-prefix}-${toKebabCase(token.path)}`;
}

function renderCssVariables(tokens: DesignTokenDefinition[]): string {
  const lines = [
    '/* stylelint-disable at-rule-empty-line-before,at-rule-name-space-after,at-rule-no-unknown */',
    '// This file is generated by scripts/build/generate-design-tokens.ts. Do not edit manually.',
    '',
    '@{html-selector} {'
  ];

  let currentLayer: DesignTokenDefinition['layer'] | undefined;
  for (const token of tokens) {
    if (token.layer !== currentLayer) {
      currentLayer = token.layer;
      lines.push('', `  // ${currentLayer} tokens`);
    }

    lines.push(`  ${toCssVariableName(token)}: ${token.lessValue};`);
  }

  lines.push('}', '');
  return lines.join('\n');
}

function renderMapping(tokens: DesignTokenDefinition[]): string {
  const lines = [
    '# Design Token Mapping',
    '',
    'CSS Variables use the `--ant-*` prefix. Tailwind preset output is intentionally out of scope for this phase.',
    '',
    '| Token path | Layer | Stability | Less variable | CSS variable | Default Less value |',
    '| --- | --- | --- | --- | --- | --- |'
  ];

  for (const token of tokens) {
    lines.push(
      `| \`${token.path}\` | ${token.layer} | ${token.stability} | ${
        token.lessVariable ? `\`${token.lessVariable}\`` : '-'
      } | \`${toCssVariableName(token).replace('@{ant-prefix}', 'ant')}\` | \`${token.lessValue}\` |`
    );
  }

  lines.push('', '## Less Compatibility Boundary', '');
  lines.push(
    'Tokens that participate in Less functions or arithmetic should keep compile-time Less values until the derived value is generated explicitly. Direct CSS property tokens may be mapped to `var(--ant-*)` in the variable theme.'
  );
  lines.push('');
  return lines.join('\n');
}

export function generateDesignTokens(): void {
  mkdirSync(path.dirname(cssVariablesPath), { recursive: true });
  mkdirSync(path.dirname(mappingPath), { recursive: true });

  writeFileSync(cssVariablesPath, renderCssVariables(designTokenDefinitions), 'utf8');
  writeFileSync(mappingPath, renderMapping(designTokenDefinitions), 'utf8');
}

generateDesignTokens();
